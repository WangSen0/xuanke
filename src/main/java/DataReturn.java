
public class DataReturn {
    private Boolean flag;
    private String msgContent;
    private Boolean xkFlag;
    private Boolean txFlag;
    private Boolean cacheFlag;

    public Boolean getFlag() {
        return flag;
    }

    public void setFlag(Boolean flag) {
        this.flag = flag;
    }

    public String getMsgContent() {
        return msgContent;
    }

    public void setMsgContent(String msgContent) {
        this.msgContent = msgContent;
    }

    public Boolean getXkFlag() {
        return xkFlag;
    }

    public void setXkFlag(Boolean xkFlag) {
        this.xkFlag = xkFlag;
    }

    public Boolean getTxFlag() {
        return txFlag;
    }

    public void setTxFlag(Boolean txFlag) {
        this.txFlag = txFlag;
    }

    public Boolean getCacheFlag() {
        return cacheFlag;
    }

    public void setCacheFlag(Boolean cacheFlag) {
        this.cacheFlag = cacheFlag;
    }
}
